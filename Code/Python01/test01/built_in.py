## int() : 정수
print(int(12.34))
print(int('56'))
print(int(True))
print(int(False))


## 2진수, 8진수, 16진수
print(int('1111',2))
print(int('77',8))
print(int('ff',16))


## float() : 실수
print(float('3'))
print(float(3.3))


